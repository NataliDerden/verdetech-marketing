def detect_categories_in_text(text: str) -> set:
    """Возвращает множество категорий, ключевые слова которых встречаются в тексте."""
    if not text:
        return set()
    blob = text.lower()
    found = set()
    for cat, keywords in CATEGORY_TRIGGERS.items():
        if any(kw in blob for kw in keywords):
            found.add(cat)
    return found


def situation_mentions_spices(situation_text: str, products_context: str = '') -> bool:
    """Возвращает True если в задаче клиента или продуктах сделки упомянуты специи/пряности."""
    blob = (situation_text + ' ' + products_context).lower()
    return any(kw in blob for kw in SPICE_TRIGGER_WORDS)


def build_catalog_index_for_prompt(industry=None, max_items=150, force_spices=False):
    """Компактный каталог для передачи Claude — с отраслевым бустом.
    force_spices=True добавляет в приоритет категории Специи / Пряности / Сушёные овощи."""
    items = []
    # Курированные всегда в индексе (это самые продаваемые)
    for p in ALL_PRODUCTS:
        items.append({
            'id': p['id'],
            'name': p['name'],
            'category': p.get('category', ''),
            'dose': p.get('dose', ''),
            'purpose': p.get('purpose', '')[:100],
        })

    # Отраслевые ключевые слова + общий приоритет
    industry_kw = INDUSTRY_BOOST_KEYWORDS.get(industry, [])
    base_kw = ['технолог', 'комплексная', 'эмультек', 'фреш', 'супер', 'умн', 'краситель', 'стабилизатор', 'маринад', 'рассол', 'витамин', 'инулин']
    priority_keywords = list(set(industry_kw + base_kw))
    if force_spices:
        priority_keywords = list(set(priority_keywords + SPICE_PRIORITY_KEYWORDS))

    # Сначала приоритетные по keywords
    added_ids = {i['id'] for i in items}
    priority = []
    regular = []
    for name, meta in _ALL_SPECS_RAW.items():
        pid = 'spec:' + name
        if pid in added_ids:
            continue
        category = meta.get('category', '')
        path = meta.get('path', '')
        if _is_client_spec(category, path):
            continue
        nm = name.lower()
        cat = category.lower()
        is_priority = any(k in nm or k in cat for k in priority_keywords)
        entry = {
            'id': pid,
            'name': name,
            'category': category,
            'dose': '',
            'purpose': get_first_line(meta.get('text', ''), 80),
        }
        if is_priority:
            priority.append(entry)
        else:
            regular.append(entry)

    # Добавляем приоритетные до max_items
    items.extend(priority[:max_items - len(items)])
    # Добиваем обычными
    remaining = max_items - len(items)
    if remaining > 0:
        items.extend(regular[:remaining])

    return items


