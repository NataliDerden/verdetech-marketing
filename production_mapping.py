def _build_trigger_index() -> dict[str, list[str]]:
    """Строит обратный индекс {нормализованный_триггер: [production_id, …]}"""
    idx: dict[str, list[str]] = {}
    for pt in PRODUCTION_TYPES:
        for trigger in pt["triggers"]:
            key = trigger.lower().strip()
            idx.setdefault(key, []).append(pt["id"])
    return idx


DETECT_TRIGGERS: dict[str, list[str]] = _build_trigger_index()


