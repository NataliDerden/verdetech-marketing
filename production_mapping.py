# Нормализуем 'ё' → 'е' для матчинга, но триггеры ищем оба варианта
_E_REPLACE = str.maketrans({'ё': 'е', 'Ё': 'Е'})


def _normalize(text: str) -> str:
    return text.lower().translate(_E_REPLACE)


def detect_production_type(text: str) -> list[str]:
    """
    Возвращает список ID типов производства, упомянутых в тексте клиента.

    >>> detect_production_type("Мы производим варёные колбасы для Магнита")
    ['meat_boiled_sausages']
    >>> detect_production_type("делаем йогурты и сырки")
    ['dairy_functional_yogurt', 'dairy_curd_desserts']
    """
    if not text:
        return []
    normalized = _normalize(text)
    found: list[str] = []
    seen: set[str] = set()
    # Проверяем все триггеры; матчим как substring (не подстроку слова)
    for trigger, pid_list in DETECT_TRIGGERS.items():
        norm_trigger = _normalize(trigger)
        # Используем word-boundary regex чтобы не матчить «кур» в «курсы»
        # но при этом учитываем, что русские слова могут быть в любой форме
        pattern = r'\b' + re.escape(norm_trigger) + r'[а-яa-z]*\b'
        if re.search(pattern, normalized) or norm_trigger in normalized:
            for pid in pid_list:
                if pid not in seen:
                    seen.add(pid)
                    found.append(pid)
    return found


def get_recommended_products(production_id: str) -> dict[str, Any]:
    """
    Возвращает структуру с рекомендациями для типа производства:
        {
            'id': str,
            'ru_name': str,
            'industry': str,
            'primary': [{name, purpose, dose}, …],
            'complementary': [{name, purpose}, …],
            'tech_reasoning': str,
            'typical_pain': str,
            'target_buyer': str,
            'roi_hook': str,
        }

    Если production_id не найден — KeyError.
    """
    for pt in PRODUCTION_TYPES:
        if pt["id"] == production_id:
            return {
                "id": pt["id"],
                "ru_name": pt["ru_name"],
                "industry": pt["industry"],
                "primary": pt.get("primary", []),
                "primary_for_dry_cured": pt.get("primary_for_dry_cured"),  # only for some
                "complementary": pt.get("complementary", []),
                "tech_reasoning": pt.get("tech_reasoning", ""),
                "typical_pain": pt.get("typical_pain", ""),
                "target_buyer": pt.get("target_buyer", ""),
                "roi_hook": pt.get("roi_hook", ""),
            }
    raise KeyError(f"Unknown production_id: {production_id}")


def list_industries() -> dict[str, list[str]]:
    """Возвращает {индустрия: [production_id, …]} — для меню КП-генератора."""
    out: dict[str, list[str]] = {}
    for pt in PRODUCTION_TYPES:
        out.setdefault(pt["industry"], []).append(pt["id"])
    return out


def list_all_recommended_skus() -> set[str]:
    """Все уникальные SKU, упомянутые в primary/complementary всех PT."""
    out: set[str] = set()
    for pt in PRODUCTION_TYPES:
        for kind in ("primary", "complementary", "primary_for_dry_cured"):
            for item in pt.get(kind, []) or []:
                out.add(item["name"])
    return out


